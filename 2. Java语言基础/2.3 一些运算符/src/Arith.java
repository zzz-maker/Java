public class Arith {
    public static void main(String[] args) {
        float number1 = 45.66f;
        int number2 = 1523;
        System.out.println((number1 - number2));
        System.out.println((number1 + number2));
        System.out.println((number1 * number2));
        System.out.println((number1 / number2));
    }
}
